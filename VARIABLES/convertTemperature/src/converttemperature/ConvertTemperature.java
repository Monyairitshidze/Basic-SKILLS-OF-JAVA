
package converttemperature;


public class ConvertTemperature {

   
    public static void main(String[] args) {
       
        //declare and assign variables
        double temperatureInCelcius = 32;
        
        //conversion
        double temperatureInFerenheit = (temperatureInCelcius*2)+30;
        
        //print output
        System.out.println(temperatureInFerenheit);
        
        
    }
    
}
