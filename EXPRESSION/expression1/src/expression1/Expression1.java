
package expression1;

public class Expression1 {

    public static void main(String[] args) {
       
        //declare two intgers and assign values
        int num1 = 5;
        int num2 = 5;
        
        //compute sum
        int sum = num1+num2;
        //compute difference
        int difference = num1-num2;
        //compute product
        int product = num1*num2;
        //compute quotient
        int quotient = num2/num1;
        
        //print 
        System.out.println("THE SUM OF TWO NUMBESR IS: "+sum);
        System.out.println("THE DIFFRENCE OF TWO NUMBESR IS: "+difference);
        System.out.println("THE PRODUCT OF TWO NUMBESR IS: "+product);
        System.out.println("THE QUOTIENT OF TWO NUMBESR IS: "+quotient);


    }
    
}
