
package perimeter;


public class Perimeter {

   
    public static void main(String[] args) {
        
        // declare and assign the length and width of rectangle
        double length = 3.5;
        double width = 2.3;
        
        //calculating the perimeter
        double perimeter = 2*(length+width);
        
        //print
        System.out.println(perimeter);
    }
    
}
